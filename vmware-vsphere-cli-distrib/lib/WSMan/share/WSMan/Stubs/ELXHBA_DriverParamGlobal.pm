package ELXHBA_DriverParamGlobal;
use WSMan::Stubs::Initializable;
use strict;


@ELXHBA_DriverParamGlobal::ISA = qw(_Initializable);


#===============================================================================
#			INITIALIZER
#===============================================================================

sub _init{
    my ($self, %args) = @_;
    unless(exists $self->{invokableMethods}){
        $self->{invokableMethods} = {};
    }
    unless(exists $self->{id_keys}){
        $self->{id_keys} = ();
    }
    $self->{paramno} = undef;
    $self->{paramname} = undef;
    $self->{lowRange} = undef;
    $self->{highRange} = undef;
    $self->{glbl} = undef;
    $self->{current} = undef;
    $self->{exportFlag} = undef;
    $self->{dynamic} = undef;
    $self->{help} = undef;
    $self->{epr_name} = undef;  
    push @{$self->{id_keys}}, 'paramno';
    @{$self->{id_keys}} = keys %{{ map { $_ => 1 } @{$self->{id_keys}} }};
    if(keys %args){
        $self->_subinit(%args);
    }
}


